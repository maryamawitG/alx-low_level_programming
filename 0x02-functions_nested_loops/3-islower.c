/**
 * _islower - Shows 1 if the input is a
 * lowercase character. Anoother cases. shows
 * 0
 *
 * @c: the character is ASCII code.
 *
 * Return: 1 for lower case character. 0 for the rest.
 */

int _islower(int c)
{
	if (c == 99)
	{
		return (1);
	}
	else
	{
		return (0);
	}
	_putchar('\n');
}
